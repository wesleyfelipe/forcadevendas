CREATE TABLE FDV_ONLINE.CLIENTE (
	ID SERIAL,
	NOME VARCHAR(255) UNIQUE NOT NULL,
	CNPJ VARCHAR(25) UNIQUE NOT NULL,
	EMAIL VARCHAR(255) UNIQUE,
	FONE VARCHAR(25) UNIQUE NOT NULL,
	SITUACAO CHAR(1) NOT NULL DEFAULT 'A',
	LIMITEPEDIDOS INTEGER DEFAULT 5000,
	DATACADASTRO DATE DEFAULT current_date,
	IDENDERECOCOMERCIAL SERIAL REFERENCES FDV_ONLINE.ENDERECO(ID) NOT NULL,
	PRIMARY KEY (ID)
);

